package class2;

public class T4ArithmeticOperators {
    public static void main(String[] args) {
        double num1=10;
        int num2=5;

        double num3=num1+num2;
        double num4=num1-num2;
        double num5=num1*num2;
        double num6=num1/num2;

        System.out.println("The add of 2 numbers "+num1+" and "+num2+" is "+ num3);
        System.out.println("The subtraction of 2 numbers "+num1+" and "+num2+" is "+ num4);
        System.out.println("The multiplication of 2 numbers "+num1+" and "+num2+" is "+ num5);
        System.out.println("The divide of 2 numbers "+num1+" and "+num2+" is "+ num6);
        System.out.println();




    }
}
